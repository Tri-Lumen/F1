/**
 * Circuit ID aliases — maps alternative/API-variant IDs to the canonical
 * key used in CIRCUITS above.
 *
 * The Jolpica (Ergast) API derives circuit IDs from Wikipedia article titles,
 * which may differ from the common city-name keys used here:
 *   - Madrid 2026: official circuit brand is "Madring"; Jolpica likely uses
 *     "madring" derived from the Wikipedia article title.
 *   - Las Vegas: API uses "las_vegas" (confirmed from 2023–2025 seasons).
 */
const CIRCUIT_ID_ALIASES: Record<string, string> = {
  madring: "madrid",       // Jolpica/Ergast ID from Wikipedia "Madring" article
  ifema_madrid: "madrid",  // Older reference name before official rebrand
};

interface CircuitMapProps {
  circuitId: string;
  circuitName?: string;
  className?: string;
  /** Show sector colours (default true) */
  showSectors?: boolean;
}

export default function CircuitMap({
  circuitId,
  className = "",
  showSectors = true,
}: CircuitMapProps) {
  const resolvedId = CIRCUIT_ID_ALIASES[circuitId] ?? circuitId;
  const circuit = CIRCUITS[resolvedId] ?? FALLBACK;
  const hasSectors = showSectors && circuit.sectors;

  // Sector colours matching F1 official style
  const sectorColors = ["#e8002d", "#ffd700", "#00d2be"];

  return (
    <svg
      viewBox={circuit.viewBox}
      className={className}
      fill="none"
      aria-hidden="true"
    >
      {hasSectors ? (
        <>
          {/* Border/outline layer — thick, low-opacity */}
          {circuit.sectors!.map((d, i) => (
            <path
              key={`border-${i}`}
              d={d}
              stroke={sectorColors[i]}
              strokeWidth="12"
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeOpacity="0.22"
              fill="none"
            />
          ))}
          {/* Track surface layer — sector coloured */}
          {circuit.sectors!.map((d, i) => (
            <path
              key={`sector-${i}`}
              d={d}
              stroke={sectorColors[i]}
              strokeWidth="5"
              strokeLinecap="round"
              strokeLinejoin="round"
              fill="none"
            />
          ))}
        </>
      ) : (
        <>
          {/* Single-colour fallback with border effect */}
          <path
            d={circuit.path}
            stroke="currentColor"
            strokeWidth="12"
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeOpacity="0.2"
            fill="none"
          />
          <path
            d={circuit.path}
            stroke="currentColor"
            strokeWidth="5"
            strokeLinecap="round"
            strokeLinejoin="round"
            fill="none"
          />
        </>
      )}

      {/* Start/finish line */}
      {circuit.startFinish && (
        <line
          x1={circuit.startFinish[0]}
          y1={circuit.startFinish[1]}
          x2={circuit.startFinish[2]}
          y2={circuit.startFinish[3]}
          stroke="currentColor"
          strokeWidth="3"
          strokeOpacity="0.9"
          strokeLinecap="round"
        />
      )}
    </svg>
  );
}

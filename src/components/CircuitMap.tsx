/** Generic oval used as fallback for unknown circuit IDs */
const FALLBACK: CircuitData = {
  viewBox: "0 0 200 130",
  path: "M 100,20 C 148,20 172,45 172,65 C 172,85 148,110 100,110 C 52,110 28,85 28,65 C 28,45 52,20 100,20 Z",
};

interface CircuitMapProps {
  circuitId: string;
  circuitName?: string;
  className?: string;
  /** Show sector colours (default true) */
  showSectors?: boolean;
}

export default function CircuitMap({
  circuitId,
  className = "",
  showSectors = true,
}: CircuitMapProps) {
  const circuit = CIRCUITS[circuitId] ?? FALLBACK;
  const hasSectors = showSectors && circuit.sectors;

  // Sector colours matching F1 official style
  const sectorColors = ["#e8002d", "#ffd700", "#00d2be"];

  return (
    <svg
      viewBox={circuit.viewBox}
      className={className}
      fill="none"
      aria-hidden="true"
    >
      {hasSectors ? (
        <>
          {/* Border/outline layer — thick, low-opacity */}
          {circuit.sectors!.map((d, i) => (
            <path
              key={`border-${i}`}
              d={d}
              stroke={sectorColors[i]}
              strokeWidth="12"
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeOpacity="0.22"
              fill="none"
            />
          ))}
          {/* Track surface layer — sector coloured */}
          {circuit.sectors!.map((d, i) => (
            <path
              key={`sector-${i}`}
              d={d}
              stroke={sectorColors[i]}
              strokeWidth="5"
              strokeLinecap="round"
              strokeLinejoin="round"
              fill="none"
            />
          ))}
        </>
      ) : (
        <>
          {/* Single-colour fallback with border effect */}
          <path
            d={circuit.path}
            stroke="currentColor"
            strokeWidth="12"
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeOpacity="0.2"
            fill="none"
          />
          <path
            d={circuit.path}
            stroke="currentColor"
            strokeWidth="5"
            strokeLinecap="round"
            strokeLinejoin="round"
            fill="none"
          />
        </>
      )}

      {/* Start/finish line */}
      {circuit.startFinish && (
        <line
          x1={circuit.startFinish[0]}
          y1={circuit.startFinish[1]}
          x2={circuit.startFinish[2]}
          y2={circuit.startFinish[3]}
          stroke="currentColor"
          strokeWidth="3"
          strokeOpacity="0.9"
          strokeLinecap="round"
        />
      )}
    </svg>
  );
}
